package Pratica01;

public class Pratica12 {
    public static void main(String[] args) {
        int dia = 4;
        switch (dia){
            case 1:
                System.out.println("DIA 1");
                break;
            case 2:
                System.out.println("Dia 2");
                break;
            case 3:
                System.out.println("DIA 3");
                break;
            case 4:
                System.out.println("Dia  4");
                break;
            default:
                System.out.println("dia invalido");


        }

    }
}
